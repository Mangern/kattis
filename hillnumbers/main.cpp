ll dp[19][10][2][2];
vector<int> sn;

ll solve(int i, int dig, bool fall, bool is_n) {
    if (i == sn.size()) return 1;

    ll& ret = dp[i][dig][fall][is_n];

    if (ret) return ret;

    for (int d = 0; d <= (is_n ? sn[i] : 9); ++d) {
        if (fall && d > dig) continue; // invalid
        bool nsn = (is_n && d == sn[i]); // if up-to-here == n and next digit is same
        bool nfall = (fall || d < dig);
        ret += solve(i+1, d, nfall, nsn);
    }
    return ret;
}

