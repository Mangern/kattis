#define FORMASK(i, mask) for (int _x = (mask), i; i = 31-__builtin_clz(_x&(-_x)), _x; _x -= (_x&-_x)) 
const int mxN = 1e4+6;
const ll MOD  = 1e9+7;

const int K = 10;

int n, m;

int nt;
ll tot;
vector<ii> adj[mxN];
ll dist[K][K];

ll dijkstra(int s, int t) {
    vll dist(n, INFLL);
    dist[s] = 0;

    priority_queue<ii, vii, greater<ii>> pq;
    pq.push({0, s});
    while (pq.size()) {
        auto [d, u] = pq.top();
        pq.pop();
        if (dist[u] < d) continue;
        if (u == t) return d;
        for (auto [v, w] : adj[u]) {
            if (dist[u] + w < dist[v]) {
                dist[v] = dist[u] + w;
                pq.push({dist[v], v});
            }
        }
    }
    return dist[t];
}

ll dp[K][1<<K];

ll solve(int u, int mask) {
    // start at u, visit all in mask and return to 0
    ll &r = dp[u][mask];
    if (r != -1) return r;

    if (mask == 0) {
        return r = dist[u][0];
    }

    r = INFLL;
    FORMASK(i, mask) {
        if (dist[u][i] == INFLL) continue;

        ll alt = solve(i, mask ^ (1<<i));
        if (alt == INFLL) continue;
        alt += dist[u][i];
        r = min(r, alt);
    }
    return r;
}

int solve_small() {
    int ret = 0;

    for (int i = 0; i <= nt; ++i) {
        memset(dp[i], -1, sizeof dp[0]);
    }

    for (int mask = 0; mask < (1<<(nt+1)); ++mask) if (!(mask&1)) {
        ll a = solve(0, mask);
        if (a <= tot) {
            ret = max(ret, __builtin_popcount(mask));
        }
    }

    return ret;
}

void testCase() {
    cin >> n >> m;

    for (int i = 0; i < n; ++i) {
        adj[i].clear();
    }

    for (int i = 0; i < K; ++i) {
        for (int j = 0; j < K; ++j) {
            if (i == j)dist[i][j] = 0;
            else dist[i][j] = INFLL;
        }
    }

    for (int i = 0; i < m; ++i) {
        ll u, v, w;
        cin >> u >> v >> w;
        adj[u].push_back({v,w});
        adj[v].push_back({u,w});
    }

    cin >> nt;

    vi treas;

    ll ans = 0;
    for (int i = 0; i < nt; ++i) {
        int p;
        cin >> p;
        if (p == 0)ans = 1;
        else {
            treas.push_back(p);
        }
    }
    nt = treas.size();

    cin >> tot;

    for (int i = 0; i < nt; ++i) {
        int t1 = treas[i];
        ll w = dijkstra(0, t1);
        dist[0][i+1] = w;
        dist[i+1][0] = w;

        for (int j = i + 1; j < nt; ++j) {
            int t2 = treas[j];
            w = dijkstra(t1, t2);
            dist[i+1][j+1] = w;
            dist[j+1][i+1] = w;
        }
    }

    ans += solve_small();
    cout << ans << endl;
}

