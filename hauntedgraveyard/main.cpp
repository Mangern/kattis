void testCase() {
    int n, m;
    cin >> n >> m;

    if (!n)exit(0);

    vector<vi> kind(n, vi(m, 0));
    int g;
    cin >> g;

    for (int i = 0; i < g; ++i) {
        int x, y;
        cin >> x >> y;
        kind[x][y] = 2;
    }

    int h;
    cin >> h;

    vector<vii> adj(n * m, vii());

    for (int i = 0; i < h; ++i) {
        int x1, y1, x2, y2, w;
        cin >> x1 >> y1 >> x2 >> y2 >> w;
        int s = x1 * m + y1;
        int t = x2 * m + y2;
        adj[s].push_back({t, w});
        kind[x1][y1] = 1;
    }

    for (int i = 0; i < n; ++i) for (int j = 0; j < m; ++j) 
        if (kind[i][j] == 0 && !(i == n - 1 && j == m - 1)) {
            for (auto [ni, nj] : vii{{i-1,j},{i+1,j},{i,j-1},{i,j+1}}) {
                if (ni < 0 || ni >= n || nj < 0 || nj >= m) continue;
                if (kind[ni][nj] == 2) continue;
                adj[i * m + j].push_back({ni * m + nj, 1});
            }
        }

    vll dist(n * m, INF);
    dist[0] = 0;

    vi p(n * m, -1);

    set<int> neg;

    for (int k = 0; k < n * m - 1; ++k) {
        for (int i = 0; i < n * m; ++i) if (dist[i] != INF) {
            for (auto [v, w] : adj[i]) {
                if (dist[i] + w < dist[v]) {
                    p[v] = i;
                    dist[v] = dist[i] + w;
                }
            }
        }
    }

    for (int i = 0; i < n * m; ++i) if (dist[i] != INF) {
        int u = i;
        for (auto [v, w] : adj[u]) {
            if (dist[u] + w < dist[v]) {
                cout << "Never" << endl;
                return;
            }
        }
    }

    if (dist[n * m - 1] == INF) {
        cout << "Impossible" << endl;
        return;
    }

    cout << dist[n * m - 1] << endl;
}

