#define sz(v) ((int)v.size())

struct FT {
	vector<ll> s;
	FT(int n) : s(n) {}
	void update(int pos, ll dif) { // a[pos] += dif
		for (; pos < sz(s); pos |= pos + 1) s[pos] += dif;
	}
	ll query(int pos) { // sum of values in [0, pos)
		ll res = 0;
		for (; pos > 0; pos &= pos - 1) res += s[pos-1];
		return res;
	}
	int lower_bound(ll sum) {// min pos st sum of [0, pos] >= sum
		// Returns n if no sum is >= sum, or -1 if empty sum is.
		if (sum <= 0) return -1;
		int pos = 0;
		for (int pw = 1 << 25; pw; pw >>= 1) {
			if (pos + pw <= sz(s) && s[pos + pw-1] < sum)
				pos += pw, sum -= s[pos-1];
		}
		return pos;
	}

    int end() {
        return sz(s);
    }
};

const int mxN = 1e6+5;
const ll MOD  = 1e9+7;

string name[mxN];
int toy[mxN];

int nxt(FT &ft, int pos, int num) {
    int n = ft.end();
    int tot = ft.query(n);

    num %= tot;

    int shere = ft.query(pos+1);
    int lb = ft.lower_bound(shere + num);

    if (lb < n) {
        return lb;
    }


    num -= tot - ft.query(pos+1);
    // advance num from 0
    num %= tot;
    assert(num!=0);
    return ft.lower_bound(num);
}

