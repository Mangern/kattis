const int mxN = 2e5+6;
const ll MOD  = 1e9+7;

struct SegTree {
     
    // contains tree
    int st[mxN * 4];
     
    // contains array elements
    int arr[mxN];
    int n;

    SegTree(int n) {
        memset(st, 0, sizeof st);
        memset(arr, 0, sizeof arr);
        this->n = n;
    }
     
    int rmq(int p, int l, int r, int i, int j) {
        if (i > r || j < l) return -1;
        if (l >= i && r <= j)return st[p];
     
        int p1 = rmq(p << 1, l, (l+r)/2, i, j);
        int p2 = rmq((p << 1) + 1, (l+r)/2 + 1, r, i, j);
     
        if (p1 == -1)return p2;
        if (p2 == -1)return p1;
        return (arr[p1] >= arr[p2]) ? p1 : p2;
    }
     
    int update(int p, int l, int r, int idx, int val) {
        int i = idx, j = idx;
        if (i > r || j < l)return st[p];
     
        if (l == i && r == j) {
            arr[i] = val;
            return st[p] = l;
        }
        int p1 = update(p<<1, l, (l+r)/2, idx, val);
        int p2 = update((p<<1)+1, (l+r)/2 + 1, r, idx, val);
        return st[p] = (arr[p1] >= arr[p2]) ? p1 : p2; 
    }
     
    // returns minimum element in range [i,j]
    int rmq(int i, int j) {
        return arr[rmq(1, 0, n-1, i, j)];
    }
     
    // sets element at index idx to val
    // returns new global min
    int update(int idx, int val) {
        return arr[update(1, 0, n-1, idx, val)];
    }
};

void testCase() {
    int n;
    cin >> n;

    vi a(n);
    vi b(n);
    cin >> a >> b;

    vi idxa(n+1);
    SegTree st(n);
    for (int i = 0; i < n; ++i) {
        idxa[a[i]] = i;
    }

    int ans = 0;
    for (int i = 0; i < n; ++i) {
        int j = idxa[b[i]];
        int nv = (j > 0 ? st.rmq(0, j - 1) : 0) + 1;
        st.update(j, nv);
        ans = max(ans, nv);
    }
    ++ans;
    cout << 2 << " " << ans << endl;
}

