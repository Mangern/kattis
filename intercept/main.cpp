const int mxN = 1e5+6;
const ll MOD  = 1e9+7;

int n, m;
int s, t;
vii adj[mxN];
vii radj[mxN];
vi nei[mxN];
ll ds[mxN];
ll dt[mxN];
bool art[mxN];
bool ind[mxN];
int dfs_num[mxN];
int dfs_low[mxN];
int counter = 1;

void dfs(int u, int p = -1) {
    dfs_num[u] = dfs_low[u] = counter++;

    for (int v : nei[u]) if (ind[v]) {
        if (!dfs_num[v]) {
            dfs(v, u);

            if (dfs_low[v] >= dfs_num[u]) {
                art[u] = 1;
            }

            dfs_low[u] = min(dfs_low[u], dfs_low[v]);
        } else if (v != p) {
            dfs_low[u] = min(dfs_low[u], dfs_num[v]);
        }
    }
}

void testCase() {
    cin >> n >> m;
    for (int i = 0; i < m; ++i) {
        ll u, v, w;
        cin >> u >> v >> w;

        adj[u].push_back({v, w});
    }

    cin >> s >> t;

    for (int i = 0; i < n; ++i) {
        ds[i] = INFLL;
        dt[i] = INFLL;

        sort(all(adj[i]));
        vii nadj;

        for (auto& pp : adj[i]) {
            if (nadj.empty() || nadj.back()[0] != pp[0]) {
                nadj.push_back(pp);
            }
        }
        adj[i] = nadj;

        for (auto [v, w] : nadj) {
            nei[i].push_back(v);
            nei[v].push_back(i);
            radj[v].push_back({i, w});
        }
    }

    priority_queue<ii, vii, greater<ii>> pq;
    ds[s] = 0;
    pq.push({0, s});

    while (pq.size()) {
        auto [d, u] = pq.top();
        pq.pop();
        if (ds[u] < d) continue;

        for (auto& [v, w] : adj[u]) {
            if (d + w < ds[v]) {
                ds[v] = d + w;
                pq.push({ds[v], v});
            }
        }
    }

    dt[t] = 0;
    pq.push({0, t});

    while (pq.size()) {
        auto [d, u] = pq.top();
        pq.pop();
        if (dt[u] < d) continue;

        for (auto& [v, w] : radj[u]) {
            if (d + w < dt[v]) {
                dt[v] = d + w;
                pq.push({dt[v], v});
            }
        }
    }

    ll mind = ds[t];
    for (int i = 0; i < n; ++i) {
        if (ds[i] + dt[i] == mind)ind[i] = 1;
    }

    dfs(t);

    vi res;

    for (int i = 0; i < n; ++i) {
        if (i == s || i == t || art[i])res.push_back(i);
    }
    cout << res << endl;
}

