const int mxN = 1e7+6;
const ll MOD  = 1e9+7;

vector<ll> primes;
bool isp[mxN];

vector<ll> big;

bool isprime(ll n) {
    if (n < mxN) return isp[n];
    for (auto p : primes) {
        if (p >= n) return true;
        if (n % p == 0) return false;
    }
    return true;
}

void solvegoldbach(ll n) {
    assert(n >= 4);
    for (auto p : primes) {
        if (p > n) break;
        if (isprime(n - p)) {
            cout << p << " " << n - p << endl;
            return;
        }
    }
    for (auto p : big) {
        if (p > n) break;
        if (isprime(n - p)) {
            cout << p << " " << n - p << endl;
            return;
        }
    }
}

void testCase() {
    ll n;
    cin >> n;

    ll i = upper_bound(all(big), n) - begin(big);

    if (i == 0) {
        cout << 3 << " ";
        solvegoldbach(n - 3);
        return;
    }

    if (i == 1 && n - big[i-1] < 4) {
        cout << 3 << " ";
        solvegoldbach(n - 3);
        return;
    } else if (n - big[i-1] < 4)--i;

    cout << big[i-1] << " ";
    solvegoldbach(n - big[i-1]);
}

