void pre() {
    memset(isp, 1, sizeof isp);
    isp[0] = isp[1] = 0;
    for (ll i = 2; i < mxN; ++i) {
        if (!isp[i]) continue;
        primes.push_back(i);
        for (ll j = i * i; j < mxN; j += i) isp[j] = 0;
    }

    for (ll k = 1; k <= 9; ++k) {
        for (ll i = 1000000*k; ; ++i) {
            if (isprime(i)) {
                big.push_back(i);
                break;
            }
        }
    }

    for (ll k = 1; k <= 9; ++k) {
        for (ll i = 10000000*k; ; ++i) {
            if (isprime(i)) {
                big.push_back(i);
                break;
            }
        }
    }

    for (ll i = 100000000; ; ++i) {
        if (isprime(i)) {
            big.push_back(i);
            break;
        }
    }
}

