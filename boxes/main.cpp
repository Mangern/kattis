const int mxN = 2e5+6;
const ll MOD  = 1e9+7;

const int K = 20;

ll n, q;
int p[K][mxN];
vi adj[mxN];
int sz[mxN];
int depth[mxN];

void dfs(int u, int cf = -1) {
    sz[u] = 1;
    for (int v : adj[u]) if (v != cf) {
        depth[v] = depth[u] + 1;
        dfs(v, u);
        sz[u] += sz[v];
    }
}

bool is_child(int u, int ch) {
    if (depth[ch] < depth[u]) return 0;

    int d = depth[ch] - depth[u];

    for (int k = 0; k < K; ++k) {
        if ((d >>k) & 1)ch = p[k][ch];
    }
    return ch == u;
}

void testCase() {
    cin >> n;

    for (int i = 0; i < n; ++i) {
        cin >> p[0][i], --p[0][i];
        if (p[0][i] >= 0) {
            adj[p[0][i]].push_back(i);
        }
    }

    for (int k = 1; k < K; ++k) {
        for (int i = 0; i < n; ++i) {
            p[k][i] = p[k-1][p[k-1][i]];
        }
    }

    for (int i = 0; i < n; ++i) {
        if (!sz[i])dfs(i);
    }

    cin >> q;

    while (q-->0) {
        int m;
        cin >> m;

        vector<int> qr(m);

        for (auto& x : qr) {
            cin >> x;
            --x;
        }

        sort(all(qr), [&] (auto a, auto b) {
            return depth[a] < depth[b];
        });

        vector<bool> inv(m, 0);

        for (int i = 0; i < m; ++i) {
            for (int j = i + 1; j < m; ++j) {
                if (is_child(qr[i], qr[j])) {
                    inv[j] = 1;
                }
            }
        }

        int ret = 0;
        for (int i = 0; i < m; ++i) if (!inv[i]) {
            ret += sz[qr[i]];
        }
        cout << ret << endl;
    }
}

