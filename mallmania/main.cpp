void testCase() {
    int n;
    cin >> n;

    if (!n)exit(0);

    vii a(n);
    for (auto& [x, y] : a)cin >> x >> y;
    a.push_back(a[0]);

    int m;
    cin >> m;

    vii b(m);
    for (auto& [x, y] : b)cin >> x >> y;
    b.push_back(b[0]);

    vii ap;
    vii bp;

    const int N = 2001;

    vector<vector<bool>> goal(N, vector<bool>(N, 0));

    for (int i = 1; i < a.size(); ++i) {
        int x = a[i-1][0];
        int y = a[i-1][1];

        int dx = (x < a[i][0] ? 1 : (x > a[i][0] ? -1 : 0));
        int dy = (y < a[i][1] ? 1 : (y > a[i][1] ? -1 : 0));

        while (true) {
            goal[x][y] = 1;
            if (ii{x, y} == a[i])break;
            x += dx;
            y += dy;
        }
    }

    queue<ii> q;
    vector<vll> dist(N, vll(N, INF));
    for (int i = 1; i < b.size(); ++i) {
        int x = b[i-1][0];
        int y = b[i-1][1];

        int dx = (x < b[i][0] ? 1 : (x > b[i][0] ? -1 : 0));
        int dy = (y < b[i][1] ? 1 : (y > b[i][1] ? -1 : 0));

        while (true) {
            if (dist[x][y] == INF) {
                dist[x][y] = 0;
                q.push({x, y});
            }
            if (ii{x, y} == b[i])break;
            x += dx;
            y += dy;
        }
    }

    ll ans = INFLL;

    while (q.size()) {
        auto [x, y] = q.front();
        q.pop();

        if (goal[x][y]) {
            ans = dist[x][y];
            break;
        }

        for (auto [nx, ny] : vii{{x-1, y}, {x+1, y}, {x, y-1}, {x, y+1}}) {
            if (nx < 0 || nx >= N || ny < 0 || ny >= N) continue;

            if (dist[nx][ny] > dist[x][y] + 1) {
                dist[nx][ny] = dist[x][y] + 1;
                q.push({nx, ny});
            }
        }
    }

    cout << ans << endl;
}

