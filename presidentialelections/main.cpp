const int mxN = 2016;
const ll MOD  = 1e9+7;

int n;
ll d[mxN];
ll c[mxN];
ll f[mxN];
ll u[mxN];
ll x[mxN];
ll dp[mxN][mxN]; // min cost to get target 

// min cost to get at least k delegates
ll solve(ll k, ll i) {
    if (k <= 0) return 0;

    if (i == 0) {
        return INF;
    }

    if (dp[k][i] != -1) return dp[k][i];

    ll r = solve(k, i - 1);
    if (x[i] != -1) {
        r = min(r, x[i] + solve(k - d[i], i - 1));
    }
    return dp[k][i] = min(r, (ll)INF);
}

void testCase() {
    cin >> n;

    for (int i = 0; i < mxN; ++i) {
        for (int j = 0; j < mxN; ++j) {
            dp[i][j] = -1;
        }
    }

    ll sm = 0;
    for (int i = 1; i <= n; ++i) {
        cin >> d[i] >> c[i] >> f[i] >> u[i];

        sm += d[i];

        if (f[i] + u[i] < c[i]) {
            x[i] = 0;
        } else if (c[i] + u[i] <= f[i]) {
            x[i] = -1;
        } else {
            x[i] = (f[i] + u[i] - c[i])/2+1;
        }

    }

    ll r = solve(sm / 2 + 1, n);

    if (r == INF) {
        cout << "impossible" << endl;
    } else {
        cout << r << endl;
    }
}

