    vi lis(n, 1);
    vi p(n, -1);

    for (int i = 0; i < n; ++i) {
        int mx = 1;
        int mxj = -1;
        for (int j = 0; j < i; ++j) {
            if (a[j] >= a[i])continue;
            if (lis[j] + 1 > mx || (lis[j] + 1 == mx && a[j] < a[mxj])) {
                mx = lis[j] + 1;
                mxj = j;
            }
        }

        lis[i] = mx;
        p[i] = mxj;
    }

